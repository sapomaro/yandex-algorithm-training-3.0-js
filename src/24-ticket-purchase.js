/*

24. Покупка билетов

Ограничение времени	1 секунда
Ограничение памяти	64Mb
Ввод	стандартный ввод или input.txt
Вывод	стандартный вывод или output.txt

За билетами на премьеру нового мюзикла выстроилась очередь из N человек, каждый из которых хочет купить 
1 билет. На всю очередь работала только одна касса, поэтому продажа билетов шла очень медленно, приводя 
«постояльцев» очереди в отчаяние. Самые сообразительные быстро заметили, что, как правило, несколько 
билетов в одни руки кассир продаёт быстрее, чем когда эти же билеты продаются по одному. Поэтому они 
предложили нескольким подряд стоящим людям отдавать деньги первому из них, чтобы он купил билеты на всех.

Однако для борьбы со спекулянтами кассир продавала не более 3-х билетов в одни руки, поэтому договориться 
таким образом между собой могли лишь 2 или 3 подряд стоящих человека.

Известно, что на продажу i-му человеку из очереди одного билета кассир тратит Ai секунд, на продажу 
двух билетов — Bi секунд, трех билетов — Ci секунд. Напишите программу, которая подсчитает минимальное 
время, за которое могли быть обслужены все покупатели.

Обратите внимание, что билеты на группу объединившихся людей всегда покупает первый из них. Также никто 
в целях ускорения не покупает лишних билетов (то есть билетов, которые никому не нужны).

Формат ввода
На вход программы поступает сначала число N — количество покупателей в очереди (1 ≤ N ≤ 5000). Далее идет 
N троек натуральных чисел Ai, Bi, Ci. Каждое из этих чисел не превышает 3600. Люди в очереди нумеруются, 
начиная от кассы.

Формат вывода

Требуется вывести одно число — минимальное время в секундах, за которое могли быть обслужены все покупатели.

Пример

Ввод
5
5 10 15
2 10 15
5 5 5
20 20 1
20 1 1

Вывод
12

*/

const fs = require('fs');

const input = fs.readFileSync('input.txt', 'utf8').toString().trim().split('\n');

const queue = [
  [Infinity, Infinity, Infinity],
  [Infinity, Infinity, Infinity],
  [Infinity, Infinity, Infinity],
];

const timings = [0, 0, 0];

let [rear, mid, front] = [0, 0, 0];

let index = 0;

for (let i = 1; i < input.length; ++i) {
  index = i + 2;

  queue[index] = input[i].split(' ').map((value) => parseInt(value));

  [rear, mid, front] = [queue[index], queue[index - 1], queue[index - 2]];

  timings[index] = Math.min(
    timings[index - 1] + rear[0],
    timings[index - 2] + mid[1],
    timings[index - 3] + front[2],
  );
}

let output = '' + timings.pop();

fs.writeFileSync('output.txt', output);
